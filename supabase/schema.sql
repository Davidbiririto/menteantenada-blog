create table if not exists public.categories (
  id bigint generated by default as identity primary key,
  name text not null,
  slug text unique not null,
  description text,
  created_at timestamptz default now()
);

create table if not exists public.posts (
  id bigint generated by default as identity primary key,
  title text not null,
  slug text unique not null,
  excerpt text,
  content text,
  featured_image_url text,
  category_id bigint references public.categories(id) on delete set null,
  status text not null default 'draft' check (status in ('draft', 'published')),
  reading_time int,
  published_at timestamptz,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

create table if not exists public.newsletter_subscribers (
  id bigint generated by default as identity primary key,
  name text,
  email text unique not null,
  created_at timestamptz default now()
);

create table if not exists public.admins (
  id bigint generated by default as identity primary key,
  email text unique not null,
  created_at timestamptz default now()
);

create or replace function public.is_admin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (select 1 from public.admins where email = (auth.jwt() ->> 'email'));
$$;

alter table public.categories enable row level security;
alter table public.posts enable row level security;
alter table public.newsletter_subscribers enable row level security;
alter table public.admins enable row level security;

drop policy if exists "Public read categories" on public.categories;
create policy "Public read categories" on public.categories for select using (true);

drop policy if exists "Admin categories full" on public.categories;
create policy "Admin categories full" on public.categories for all using (public.is_admin()) with check (public.is_admin());

drop policy if exists "Public read published posts" on public.posts;
create policy "Public read published posts" on public.posts for select using (status = 'published' or public.is_admin());

drop policy if exists "Admin posts full" on public.posts;
create policy "Admin posts full" on public.posts for all using (public.is_admin()) with check (public.is_admin());

drop policy if exists "Public insert newsletter" on public.newsletter_subscribers;
create policy "Public insert newsletter" on public.newsletter_subscribers for insert with check (true);

drop policy if exists "Admin read newsletter" on public.newsletter_subscribers;
create policy "Admin read newsletter" on public.newsletter_subscribers for select using (public.is_admin());

drop policy if exists "Admin delete newsletter" on public.newsletter_subscribers;
create policy "Admin delete newsletter" on public.newsletter_subscribers for delete using (public.is_admin());

drop policy if exists "Admin read admins" on public.admins;
create policy "Admin read admins" on public.admins for select using (public.is_admin());

drop policy if exists "Admin manage admins" on public.admins;
create policy "Admin manage admins" on public.admins for all using (public.is_admin()) with check (public.is_admin());

